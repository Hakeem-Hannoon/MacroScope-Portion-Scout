/**
 * Regenerate nutrition/label-map.json from the shared food set (./foods.mjs) —
 * the classifier-label → FDC description map the NutrientStore resolves by exact
 * match (the quality-critical artifact; REAL_ADAPTERS.md §1). Generated, not
 * hand-edited, so it can never drift from the vocabulary or the nutrient bundle.
 * Each food contributes its vocab word, its FDC description, and any aliases.
 * Unmapped labels still fall back to the store's FTS/LIKE search.
 *
 *   node nutrition/starter/build-label-map.mjs
 */
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { FOODS } from "./foods.mjs";

const out = fileURLToPath(new URL("../label-map.json", import.meta.url));

const map = {
  _comment:
    "GENERATED by nutrition/starter/build-label-map.mjs from foods.mjs — do not hand-edit. Classifier-label → FDC description; the NutrientStore resolves these by exact match, falling back to FTS/LIKE for anything unmapped. The vocabulary is a common subset of FoodSeg103 (see docs/REAL_ADAPTERS.md §1).",
};
for (const f of FOODS) {
  const keys = new Set([f.vocab, f.desc.toLowerCase(), ...(f.aliases ?? [])]);
  for (const k of keys) map[k] = f.desc;
}

writeFileSync(out, JSON.stringify(map, null, 2) + "\n");
const n = Object.keys(map).length - 1;
console.log(`wrote ${out}: ${FOODS.length} foods → ${n} label keys`);
